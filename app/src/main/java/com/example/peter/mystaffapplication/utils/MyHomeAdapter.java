package com.example.peter.mystaffapplication.utils;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;
import android.support.v4.view.PagerAdapter;
import android.view.View;
import android.view.ViewGroup;

import java.util.List;

/**
 * Created by peter on 2016/12/28.
 */

public class MyHomeAdapter extends FragmentPagerAdapter {
    List<Fragment> list;

    public MyHomeAdapter(FragmentManager fm) {
        super(fm);
    }

    public MyHomeAdapter(FragmentManager fm,List<Fragment> list) {
        super(fm);
        this.list=list;
    }

    @Override
    public Fragment getItem(int position) {
        return list.get(position);
    }

    @Override
    public int getCount() {
        return list.size();
    }
}
